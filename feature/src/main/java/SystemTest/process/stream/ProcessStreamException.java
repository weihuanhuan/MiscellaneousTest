package SystemTest.process.stream;

public class ProcessStreamException extends RuntimeException {

    public ProcessStreamException() {
    }

    public ProcessStreamException(String message) {
        super(message);
    }

    public ProcessStreamException(String message, Throwable cause) {
        super(message, cause);
    }

    public ProcessStreamException(Throwable cause) {
        super(cause);
    }

}


